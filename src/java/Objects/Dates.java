package Objects;

import java.util.Date;

/**
 *
 * @author ASUS
 */
public class Dates {
  private int id_date;
  private Date date;

    /**
     * @return the id_date
     */
    public int getId_date() {
        return id_date;
    }

    /**
     * @param id_date the id_date to set
     */
    public void setId_date(int id_date) {
        this.id_date = id_date;
    }

    /**
     * @return the date
     */
    public Date getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(Date date) {
        this.date = date;
    }
}
